local opt = vim.opt

-- Sets tab spacing to be 4 spaces
opt.expandtab = true
opt.tabstop = 4
opt.softtabstop = 4
opt.shiftwidth = 4

-- Enable Clipboard support
opt.clipboard = "unnamedplus"

-- Add relative line numbers
opt.number = true
opt.relativenumber = true

-- Splits right & below
opt.splitbelow = true
opt.splitright = true

